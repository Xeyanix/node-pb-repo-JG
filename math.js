// kalulator js w Node

function add(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function mul(a, b) {
    return a * b;
}

function div(a, b) {
    return a / b;
}

module.exports.add = add;
module.exports.sub = sub;
module.exports.mul = mul;
module.exports.div = div;
